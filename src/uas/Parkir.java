    private void waktuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_waktuActionPerformed
        try {
            // TODO add your handling code here:
            Connection connection = Koneksi.ConnectDB();
            String query = "SELECT * FROM tb_parkir where platnomor='"+PlatNomor2.getText()+"'";
            Statement st = connection.createStatement();
            ResultSet rs = st.executeQuery(query);
            String type = null;
            while(rs.next()){
                type = rs.getString("typeKendaraan");
            }
            st.close();
            int sum = 0;
            if(type == null || waktu.getText().isBlank() || waktu.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Invalid", "ERROR", JOptionPane.ERROR_MESSAGE);
            }else{
                
                if(type.equals("Motor")){
                    sum = 5000 * Integer.parseInt(waktu.getText());
                }
                if(type.equals("Mobil")){
                    sum = 10000 * Integer.parseInt(waktu.getText());
                }
                
                if(sum > 25000){
                    sum = 25000;
                }
                
                harga.setText(String.valueOf(sum));
               
            }
        } catch (SQLException ex) {
            Logger.getLogger(Parkir.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_waktuActionPerformed

