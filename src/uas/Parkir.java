    public int hitungHarga(String type, int waktu){
        int hasil = 0; 
        if(type.equals("Motor")){
            hasil += 5000 * waktu;
            return hasil > 25000? 25000 : hasil;
         }else{
            hasil += 10000 * waktu;
            return hasil > 25000? 25000 : hasil;
         }
    }
    
